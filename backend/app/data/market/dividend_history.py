def _verdict(prev, cur) -> str | None:
    """직전 대비 배당 변화 판정."""
    if prev is None or cur is None:
        return None
    if cur == 0 and prev > 0:
        return "중단"
    if cur < prev * 0.98:
        return "삭감"
    if cur > prev * 1.02:
        return "증가"
    return "유지"


def crisis_dividends(ticker: str) -> dict | None:
    """종목의 3대 위기 배당 내역. 큐레이션에 없으면 None.

    반환: {"available": True, "notes":..., "sources":[...],
           "crises":[{"key","label","rows":[{year, dps, verdict}], "summary"}]}
    """
    rec = CURATED.get(ticker)
    if not rec:
        return None
    dps = rec.get("dps", {})
    out_crises = []
    for c in CRISES:
        rows = []
        prev = None
        have_any = False
        for y in c["years"]:
            v = dps.get(y)
            if v is not None:
                have_any = True
            rows.append({"year": y, "dps": v, "verdict": _verdict(prev, v)})
            if v is not None:
                prev = v
        if not have_any:
            # 이 위기 구간 데이터가 전혀 없으면 표시하되 비었음을 알림
            out_crises.append({"key": c["key"], "label": c["label"], "rows": rows,
                               "summary": "해당 시기 데이터 없음(상장 전 또는 미확인)"})
            continue
        # 요약: 위기 구간 내 최소→최대, 삭감 여부
        vals = [r["dps"] for r in rows if r["dps"] is not None]
        cut = any(r["verdict"] in ("삭감", "중단") for r in rows)
        summary = ("위기 중 배당 삭감 이력 있음" if cut
                   else "위기에도 배당 유지/증가")
        out_crises.append({"key": c["key"], "label": c["label"], "rows": rows,
                           "summary": summary, "min": min(vals) if vals else None,
                           "max": max(vals) if vals else None})
    return {
        "available": True,
        "name": rec.get("name"),
        "notes": rec.get("notes"),
        "sources": rec.get("sources", []),
        "crises": out_crises,
    }


def covered_tickers() -> list[str]:
    return list(CURATED.keys())
